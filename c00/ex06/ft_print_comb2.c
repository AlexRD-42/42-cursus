#include <unistd.h>

void ft_print_comb2(void)
{
	int i=0, j=0;
	char val[5];
	char s[2];
	val[2] = ' ';
	s[0] = ',';
	s[1] = ' ';

	while (i<100)
	{
		j = i+1;
		while (j<100)
		{
			val[0] = i/10 + '0';
			val[1] = i%10 + '0';
			val[3] = j/10 + '0';
			val[4] = j%10 + '0';
			j++;
			
			write(1, &val[0], 5);
			if (!(i==98 && j==100))
			{
			    write(1, &s[0], 2);
			}
		}
		i++;
	}
}

int main (void)
{
	ft_print_comb2();
}
